text = 'I am the best in the world'

i = 0

while i < len(text):
        if i == 'a':
                print(text[i], '- ')
                print(i)
                i += 1
                continue
        if i == 'e':
                print(text[i], '- ')
                print(i)
                i += 1
                continue
        if i == 'i':
                print(text[i], '- ')
                print(i)
                i += 1
                continue
        if i == 'o':
                print(text[i], '- ')
                print(i)
                i += 1
                continue
        if i == 'u':
                print(text[i], '- ')
                print(i)
                i += 1
                continue
        else:
                continue
